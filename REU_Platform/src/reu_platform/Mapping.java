    private void deleteColumnButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteColumnButtonActionPerformed
        DefaultTreeModel model = (DefaultTreeModel)globalJTree.getModel();
        
        //Get the last node selected
        DefaultMutableTreeNode node = (DefaultMutableTreeNode)globalJTree.getLastSelectedPathComponent();
        //Remove node
        model.removeNodeFromParent(node);
        globalTree.reload();
        globalJTree.revalidate();
    }//GEN-LAST:event_deleteColumnButtonActionPerformed

