    private void addCheckButtonActionPerformed(java.awt.event.ActionEvent evt) {    
        //Make a linked list of all the check boxes in the selected tab.
        LinkedList<JCheckBox> checkBoxes = new LinkedList<JCheckBox>();
        for( Component comp : sourceList.get(sourcesTabPane.getSelectedIndex()).checkBoxPanel.getComponents() )
        { if( comp instanceof JCheckBox) checkBoxes.add( (JCheckBox)comp ); }
        
        DefaultTreeModel model = (DefaultTreeModel)globalJTree.getModel();
        DefaultMutableTreeNode node = (DefaultMutableTreeNode)globalJTree.getLastSelectedPathComponent();
        DefaultMutableTreeNode tempChild;
        
        for(int i = 0 ; i < checkBoxes.size(); i++)
        {
            if(checkBoxes.get(i).isSelected())
            {
                tempChild = new 
                        DefaultMutableTreeNode(checkBoxes.get(i).getText());
                node.add(tempChild);   
                model.insertNodeInto(tempChild, node, node.getChildCount()-1);
            }
        }
        //Get the last node selected
        globalTree.reload();
        globalJTree.revalidate();
        
    } 
    
